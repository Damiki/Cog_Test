$(document).ready(function () {

    $("#toggleInertia").click(function () {
        $("#disappearInertia").toggleClass("non-disappear");
    });

    $("#toggleLoad").click(function () {
        $("#disappearLoad").toggleClass("non-disappear");
    });

    $("#toggleAssembler").click(function () {
        $("#disappearAssembler").toggleClass("non-disappear");
    });


});